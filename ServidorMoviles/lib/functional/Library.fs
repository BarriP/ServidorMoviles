namespace Magic

module ImageUtils =
    let checkMagicNumbers array =
        match array with
        | 0xFF::0xD8::0xFF::tail -> "jpeg"
        | 0x47::0x49::0x46::0x38::tail -> "gif"
        | 0x89::0x50::0x4E::0x47::0x0D::0x0A::0x1A::0x0A::tail -> "png"
        | 0x42::0x4D::tail -> "bmp"
        | _ -> ""
