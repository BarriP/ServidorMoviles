namespace Magic

module ImageUtils =
    let checkMagicNumbers array =
        match array with
        | [0xFF;0xD8;0xFF;_;_;_;_;_] -> "jpeg"
        | [0x47;0x49;0x46;0x38;_;_;_;_] -> "gif"
        | [0x89;0x50;0x4E;0x47;0x0D;0x0A;0x1A;0x0A] -> "png"
        | [0x42;0x4D;_;_;_;_;_;_] -> "bmp"
        | _ -> ""
